package src;

public class Usuarios{

    public String User1;
    public String User2;
    public String User3;

    public Usuarios(String user1, String user2, String user3) {


        User1 = user1;
        User2 = user2;
        User3 = user3;
    }

    public String getUser1() {
        return User1;
    }

    public void setUser1(String user1) {
        User1 = user1;
    }

    public String getUser2() {
        return User2;
    }

    public void setUser2(String user2) {
        User2 = user2;
    }

    public String getUser3() {
        return User3;
    }

    public void setUser3(String user3) {
        User3 = user3;
    }
}
