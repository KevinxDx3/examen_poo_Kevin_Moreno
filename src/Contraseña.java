package src;

public class Contraseña {

    public double contraseña1;
    public double contraseña2;
    public double contraseña3;

    public Contraseña(double contraseña1, double contraseña2, double contraseña3) {
        this.contraseña1 = contraseña1;
        this.contraseña2 = contraseña2;
        this.contraseña3 = contraseña3;
    }

    public double getContraseña1() {
        return contraseña1;
    }

    public void setContraseña1(double contraseña1) {
        this.contraseña1 = contraseña1;
    }

    public double getContraseña2() {
        return contraseña2;
    }

    public void setContraseña2(double contraseña2) {
        this.contraseña2 = contraseña2;
    }

    public double getContraseña3() {
        return contraseña3;
    }

    public void setContraseña3(double contraseña3) {
        this.contraseña3 = contraseña3;
    }
}
