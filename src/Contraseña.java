package src;

public class Contraseña {

    public int contraseña1;
    public int contraseña2;
    public int contraseña3;

    public Contraseña(int contraseña1, int contraseña2, int contraseña3) {
        this.contraseña1 = contraseña1;
        this.contraseña2 = contraseña2;
        this.contraseña3 = contraseña3;
    }

    public int getContraseña1() {
        return contraseña1;
    }

    public void setContraseña1(int contraseña1) {
        this.contraseña1 = contraseña1;
    }

    public int getContraseña2() {
        return contraseña2;
    }

    public void setContraseña2(int contraseña2) {
        this.contraseña2 = contraseña2;
    }

    public int getContraseña3() {
        return contraseña3;
    }

    public void setContraseña3(int contraseña3) {
        this.contraseña3 = contraseña3;
    }
}
